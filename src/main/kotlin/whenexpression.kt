fun main(args: Array<String>) {
    var mychar='E'

    when(mychar){
        'A'-> println("A is a vovel")
        'E'-> println("E is a vovel")
        'I'-> println("I is a vovel")
        'O'-> println("O is a vovel")
        'U'-> println("U is a vovel")

        else -> println("$mychar is a consonant")
    }
}